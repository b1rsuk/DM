package org.example;

public class Combination {
    private boolean a;
    private boolean b;
    private boolean c;

    public Combination(boolean a, boolean b, boolean c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public boolean isA() {
        return a;
    }

    public boolean isB() {
        return b;
    }

    public boolean isC() {
        return c;
    }
}
